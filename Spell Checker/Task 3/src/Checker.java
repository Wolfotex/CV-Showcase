    // swap letter and the next letter of a string
    public String swap(String word, int i){
        char[] characters = word.toCharArray();
        char temp = characters[i];
        characters[i] = characters[i + 1];
        characters[i + 1] = temp;
        return new String(characters);
    }

    // insert a character to a given position of a string
    public String insert(String word, int i, char c){
        StringBuilder sb = new StringBuilder(word);
        sb.insert(i, c);
        return sb.toString();
    }

    // delete a character of a string at a given position of a string
    public String delete(String word, int i){
        StringBuilder sb = new StringBuilder(word);
        sb.deleteCharAt(i);
        return sb.toString();
    }

    // replace a character at a given position with the given character
    public String replace(String word, int i, char character){
        char[] characters = word.toCharArray();
        characters[i] = character;
        return new String(characters);
    }

    public String phoneticSub(String word) {
        // convert the word to uppercase and remove any non-letter characters
        word = word.toUpperCase().replaceAll("[^A-Z]", "");

        if (word.length() == 0) {
            return "";
        }

        StringBuilder sb = new StringBuilder();
        char[] chars = word.toCharArray();

        // retain the first letter of the word
        sb.append(chars[0]);

        // replace consonants with digits according to a mapping
        for (int i = 1; i < chars.length; i++) {
            char c = chars[i];
            switch (c) {
                case 'B':
                case 'F':
                case 'P':
                case 'V':
                    sb.append('1');
                    break;
                case 'C':
                case 'G':
                case 'J':
                case 'K':
                case 'Q':
                case 'S':
                case 'X':
                case 'Z':
                    sb.append('2');
                    break;
                case 'D':
                case 'T':
                    sb.append('3');
                    break;
                case 'L':
                    sb.append('4');
                    break;
                case 'M':
                case 'N':
                    sb.append('5');
                    break;
                case 'R':
                    sb.append('6');
                    break;
            }
        }

        // remove consecutive digits and zeros
        StringBuilder result = new StringBuilder();
        char[] digits = sb.toString().toCharArray();
        result.append(digits[0]);
        for (int i = 1; i < digits.length; i++) {
            if (digits[i] != digits[i - 1] && digits[i] != '0') {
                result.append(digits[i]);
            }
        }

        // append the result to 4 digits
        while (result.length() < 4) {
            result.append('0');
        }
        return result.toString().substring(0, 4);
    }


    public LinkedList checkForSpellings(String word){
        LinkedList suggestedWords = new LinkedList();
        String temp;

        word = word.toLowerCase();

        // if the user typed the word correctly
        if (words.contains(word)){
            LinkedList.insert(suggestedWords, word);
            return suggestedWords;
        }

        // when the user typed the letter in the incorrect order
        for (int i = 0; i < word.length() - 1; i++){
            temp = swap(word, i);
            if (words.contains(temp)){
                LinkedList.insert(suggestedWords, temp);
            }
        }

        // when the user typed the word a letter short
        for (int i = 0; i < word.length() + 1; i++){
            for (char c = 'a'; c <= 'z'; c++){
                temp = insert(word, i, c);
                if (words.contains(temp)){
                    LinkedList.insert(suggestedWords, temp);
                }
            }
        }

        // when the user types the word with an extra letter
        for (int i = 0; i < word.length(); i++){
            temp = delete(word, i);
            if (words.contains(temp)){
                LinkedList.insert(suggestedWords, temp);
            }
        }

        // check spellings by replacing each letter of the word with a-z
        for (int i = 0; i < word.length(); i++){
            for (char c = 'a'; c <= 'z'; c++){
                temp = replace(word, i, c);
                if (words.contains(temp)){
                    LinkedList.insert(suggestedWords, temp);
                }
            }
        }

        if (words.contains(phoneticSub(word))){
            LinkedList.insert(suggestedWords, phoneticSub(word));
        }

        if (suggestedWords.isEmpty()){
            LinkedList.insert(suggestedWords, "Your word is not in our dictionary.");
        }

        return suggestedWords;
    }
